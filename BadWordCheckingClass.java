    public boolean checkUsingBanWord(String contents){
        boolean IsUsingBanWord = false;

        Collection list = banList();
        String result = contents.replaceAll("(\\b[가-힣]+)[!@#$%^&*()_+=-]+([가-힣]+\\b)+", "$1$2")
                .replaceAll("(\\b[가-힣]+)\\d+([가-힣]+\\b)", "$1$2");

        Trie.TrieBuilder trieBuilder = Trie.builder().onlyWholeWordsWhiteSpaceSeparated().ignoreCase();
        trieBuilder.addKeywords(list);
        Trie build = trieBuilder.build();

        Collection<Emit> emits = build.parseText(result);

        if(emits.size()>0){
            IsUsingBanWord = true;
        }

        return  IsUsingBanWord;

    }

